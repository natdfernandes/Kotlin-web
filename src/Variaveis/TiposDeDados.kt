package Variaveis

fun main(){
    //Tipo númericos
    val byte: Byte = 8
    val short: Short = 360
    val int: Int = 1000
    val long: Long = 10000L
    val float: Float = 150.10F
    val double: Double = 300.10

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    //Tipo Booleano
    //True False
    val boolean: Boolean = false

    println(boolean)

    //Tipos textuais
    val char: Char = 'F'
    val string: String = "Fonseca"

    println(char)
    println(string)
}